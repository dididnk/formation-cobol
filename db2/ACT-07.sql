    SELECT EMP_NOM AS "EMPLOYÉ(E)S",
       DATE_EMB AS "DATE D'EMBAUCHE"
    FROM EMPLOYEE WHERE DEPT_NUM =    
    (SELECT DEPT_NUM FROM EMPLOYEE WHERE UPPER(EMP_NOM)='JEAN')
    EXCEPT
    (SELECT EMP_NOM,DATE_EMB 
       FROM EMPLOYEE WHERE UPPER(EMP_NOM)='JEAN');

