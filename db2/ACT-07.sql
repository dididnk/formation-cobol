--*                    A07:  EXERCICE 02                               *  
--**********************************************************************

    SELECT EMP_NUM AS "MATRICULE", 
       EMP_NOM AS "EMPLOYÉ(E)S",
       SAL AS "SALAIRES"
    FROM EMPLOYEE WHERE SAL >
    (SELECT AVG(SAL) FROM EMPLOYEE) ORDER BY SAL DESC;

